// Arrays en JavaScript

let frutas = ['pera','manzana','naranja'];
console.log(frutas);

// Para acceder a los valores de un Array
// utilizamos su índice (que comienza en 0)

// Para leer valores [posición ó expresión]

console.log(frutas[0]);

console.log(frutas[frutas.length -1]);

// Para escribir (setear) valores 

frutas[1] = 'albaricoque';
console.log(frutas);